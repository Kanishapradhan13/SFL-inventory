import "@/styles/globals.css";
import type { Metadata } from "next";
import { Providers } from "./providers";
import { fontSans } from "@/config/fonts";
import { redirect } from "next/navigation";
import clsx from "clsx";
import { cookies } from "next/headers";
import { Flip, ToastContainer, Zoom, toast } from "react-toastify";
import "react-toastify/dist/ReactToastify.css";
export const metadata: Metadata = {
  title: "Next.js",
  description: "Generated by Next.js",
};
import { GlobalProvider } from "@/contex";

export default async function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const res = await fetch("http://localhost:5000/auth/auth-storekeeper", {
    headers: {
      Cookie: `auth_session=${cookies().get("auth_session")?.value ?? ""}`,
    },
  });

  if (!res.ok) {
    console.log("Failed to authorize storekeeper!");
    redirect("/login");
  }
  const userData = res && (await res.json());

  return (
    <html lang="en">
      <body className={clsx("font-sans antialiased", fontSans.className)}>
        <GlobalProvider user={userData}>
          <Providers>
            {children}
            <ToastContainer
              position="top-center"
              autoClose={5000}
              hideProgressBar={false}
              newestOnTop={false}
              closeOnClick
              rtl={false}
              pauseOnFocusLoss
              draggable
              pauseOnHover
              theme="light"
              transition={Flip}
           
            />
          </Providers>
        </GlobalProvider>
      </body>
    </html>
  );
}
